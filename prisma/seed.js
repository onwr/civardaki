async function upsertParentCategory(parentData, index) {
  const parentSlug = slugifyTR(parentData.name);

  return prisma.category.upsert({
    where: { slug: parentSlug },
    update: {
      name: parentData.name,
      description: parentData.description || null,
      icon: parentData.icon || null,
      color: parentData.color || null,
      level: 0,
      path: parentSlug,
      sortOrder: index,
      isActive: true,
      isFeatured: !!parentData.featured,
      keywords: parentData.keywords || null,
    },
    create: {
      name: parentData.name,
      slug: parentSlug,
      description: parentData.description || null,
      icon: parentData.icon || null,
      color: parentData.color || null,
      level: 0,
      path: parentSlug,
      sortOrder: index,
      isActive: true,
      isFeatured: !!parentData.featured,
      keywords: parentData.keywords || null,
    },
  });
}

async function upsertChildCategory(parent, childName, index) {
  const rawChildSlug = slugifyTR(childName);
  const uniqueChildSlug = `${parent.slug}-${rawChildSlug}`;

  return prisma.category.upsert({
    where: { slug: uniqueChildSlug },
    update: {
      name: childName,
      parentId: parent.id,
      level: 1,
      path: `${parent.slug}/${rawChildSlug}`,
      sortOrder: index,
      isActive: true,
      isFeatured: false,
    },
    create: {
      name: childName,
      slug: uniqueChildSlug,
      parentId: parent.id,
      level: 1,
      path: `${parent.slug}/${rawChildSlug}`,
      sortOrder: index,
      isActive: true,
      isFeatured: false,
    },
  });
}

async function main() {
  console.log("Final kategori seed başlıyor...");

  for (let i = 0; i < categoryTree.length; i++) {
    const parentData = categoryTree[i];
    const parent = await upsertParentCategory(parentData, i);

    for (let j = 0; j < parentData.children.length; j++) {
      await upsertChildCategory(parent, parentData.children[j], j);
    }
  }

  const totalCategories = await prisma.category.count();
  console.log(`Seed tamamlandı. Toplam kategori sayısı: ${totalCategories}`);
}

main()
  .catch((e) => {
    console.error("Seed error:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });